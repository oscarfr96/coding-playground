puntaje =  97

# Se evalua de arriba hacia abajo, por lo que el orden es importante
if puntaje >= 90:
    #Identacion/Sangrado: Python usa la indentación para definir el bloque de código
    print("A")
elif puntaje >= 80:
    print("B")
elif puntaje >= 70:
    print("C")
elif puntaje >= 60:
    print("D")
else:
    print("F")